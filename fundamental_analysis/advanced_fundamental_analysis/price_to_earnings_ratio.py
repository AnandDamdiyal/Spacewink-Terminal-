def calculate_price_to_earnings_ratio(price_per_share, earnings_per_share):
    """
    Calculates the price-to-earnings (P/E) ratio given the price per share and earnings per share.

    Args:
        price_per_share (float): The price per share of the stock.
        earnings_per_share (float): The earnings per share of the stock.

    Returns:
        float: The P/E ratio of the stock.
    """
    return price_per_share / earnings_per_share
   
